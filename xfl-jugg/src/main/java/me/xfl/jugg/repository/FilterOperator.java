package me.xfl.jugg.repository;

/**
 * Filter operator.
 * 
 * @author <a href="mailto:DL88250@gmail.com">Liang Ding</a>
 * @version 1.0.0.1, Oct 18, 2011
 * @see Filter
 */
public enum FilterOperator {

	/**
	 * Less than.
	 */
	LESS_THAN,
	/**
	 * Less than or equal.
	 */
	LESS_THAN_OR_EQUAL,
	/**
	 * Greater than.
	 */
	GREATER_THAN,
	/**
	 * Grater than or equal.
	 */
	GREATER_THAN_OR_EQUAL,
	/**
	 * Equal.
	 */
	EQUAL,
	/**
	 * Not equal.
	 */
	NOT_EQUAL,
	/**
	 * In.
	 */
	IN,
}
